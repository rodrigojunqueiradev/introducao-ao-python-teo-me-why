# %%

# SOMA
print("1 + 1 = ", 1 + 1)

# SUBTRAÇÃO
print("10 - 5 = ", 10 - 5)

# MULTIPLICAÇÃO
print("10 * 5 = ", 10 * 5)

# DIVISÃO
print("10 / 5 = ", 10 / 5)

# DIVISÃO com resultado apenas da parte inteira
print("10 // 3 = ", 10 // 3)

# RESTO DA DIVISÃO
print("10 % 4 = ", 10 % 4)

# POTÊNCIA
print("3 ** 3 = ", 3 ** 3)

